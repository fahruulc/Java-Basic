public class Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        
        // Penjumlahan
        int hasilTambah = a + b;
        System.out.println("Hasil penjumlahan a + b adalah " + hasilTambah);
        
        // Pengurangan
        int hasilKurang = a - b;
        System.out.println("Hasil pengurangan a - b adalah " + hasilKurang);
        
        // Perkalian
        int hasilKali = a * b;
        System.out.println("Hasil perkalian a * b adalah " + hasilKali);
        
        // Pembagian
        int hasilBagi = a / b;
        System.out.println("Hasil pembagian a / b adalah " + hasilBagi);
    }
}
